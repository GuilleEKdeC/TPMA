    @Override
    public int print(Graphics graf, PageFormat pagfor, int index) throws PrinterException {
        
        if(index > 0){  //index hace referencia a la cantidad de pag. aca solo se imprime una. Si se vuelve a llamar luego de que se imprimio una pag, termina
            return NO_SUCH_PAGE;
        }
        
        Graphics2D hub = (Graphics2D) graf;
        hub.translate(pagfor.getImageableX() + 30, pagfor.getImageableY() + 30); //para que no se pegue a los borde de la hoja, sino que haya un margen
        hub.scale(1.0, 1.0);
        
        //Ahora indicamos que se imprima solo el panel "comprobante"
        comprobante.printAll(graf);
        return PAGE_EXISTS;
    }
